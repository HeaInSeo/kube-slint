// profileCandidate is one SLI a profile expects to see in a measurement
// summary, along with the default threshold rule recommend-policy generates
// for it and the reasoning inspect/recommend-policy print to the user.
type profileCandidate struct {
	ID       string
	Operator string
	Value    float64
	Reason   string

	// Tier is one of:
	//   "core"          — always recommended as an active threshold once
	//                      measured, regardless of --strictness.
	//   "noisy"         — CI-environment sensitive; --strictness governs
	//                      whether it becomes an active threshold or stays
	//                      commented out.
	//   "informational" — a raw activity counter with no principled
	//                      pass/fail threshold (e.g. a total-requests
	//                      count); always shown to the user but never
	//                      promoted to an active rule, regardless of
	//                      --strictness or measurement.
	Tier string
}

const (
	tierCore          = "core"
	tierNoisy         = "noisy"
	tierInformational = "informational"
)

var validProfileOperators = map[string]bool{
	"<": true, "<=": true, ">": true, ">=": true, "==": true,
}

var validProfileTiers = map[string]bool{
	tierCore: true, tierNoisy: true, tierInformational: true,
}

